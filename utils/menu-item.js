export const getProductById = (id) => {
  for (let i = 0; i < menuItem.length; i++) {
    const menu = menuItem[i];
    for (let j = 0; j < menu.submenu.length; j++) {
      const subCategory = menu.submenu[j];
      const product = subCategory.submenu.find((product) =>
        product.details.find((p) => p.id === Number(id))
      );
      if (product) {
        return product.details.find((p) => p.id === Number(id));
      }
    }
  }
  return null; // product not found
};
export const getProductByBrandName = (menuItems, title) => {
  const productTitle = title.productTitle;
  for (const menuItem of menuItems) {
    if (menuItem.submenu) {
      for (const product of menuItem.submenu) {
        if (product.title === productTitle) {
          return product;
        } else {
          const subProduct = getProductByBrandName([product], title);
          if (subProduct) {
            return subProduct;
          }
        }
      }
    }
  }
  return null;
};
